package week1.day2;

public class LearnContinue {

	public static void main(String[] args) {
		
		//print the numbers from 1 to 5
		//skip the iteration no 3
		
		for (int i = 1; i <=5; i++) {
			
			if (i==4) {
				
				continue;
				
			}
			System.out.println(i);
			
			
			
		}

	}

}
